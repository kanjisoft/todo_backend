package com.todoapp.controller;

import java.util.Date;

public class CalendarEntryShort {

	private Date start;

	public CalendarEntryShort() {
	}

	public CalendarEntryShort(Date start) {
		super();
		this.start = start;
	}

	public Date getStart() {
		return start;
	}
	public void setStart(Date start) {
		this.start = start;
	}
	
	
}
